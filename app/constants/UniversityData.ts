export const LISTA_UNIVERSIDADES = Object.keys(UNIVERSIDADES);

export const getCostoUniversidad = (nombre: string, year: number): number => {
    const dataUni = UNIVERSIDADES[nombre];
    if (!dataUni) return 0;

    if (dataUni[year]) return dataUni[year];

    // Si el año pedido es mayor al máximo (2042), devolvemos el último disponible
    // Si es menor al mínimo (2024), devolvemos el primero
    const years = Object.keys(dataUni).map(Number).sort((a, b) => a - b);
    const minYear = years[0];
    const maxYear = years[years.length - 1];

    if (year > maxYear) {
        const costMax = dataUni[maxYear];
        const costPrev = dataUni[years[years.length - 2]];

        if (costMax && costPrev && years.length >= 2) {
            const inflationRate = costMax / costPrev;
            const yearsDiff = year - maxYear;
            // Proyectar el costo usando la inflación del último año disponible
            return costMax * Math.pow(inflationRate, yearsDiff);
        }
        return costMax;
    }
    if (year < minYear) return dataUni[minYear];

    // Fallback seguro
    return dataUni[minYear] || 0;
};